​
function valuesToString(object) {
 var something = "";
  var count = 1;
  for(var key in object) {
        if (count == 1 && typeof object[key] === 'string'){
        something += object[key]; 
        count++;
      } else if (typeof object[key] === 'string'){
        something += ' ' + object[key];
      }
  }
  return something;
  }
  
